public class v06_IncAndDec_Operator {
    public static void main(String[]args){
        System.out.println("Increasment And Decrease Operator");
        int a =56;
        System.out.println(a++);   // First Use Value Then Increase
        System.out.println(a);

        System.out.println(++a);   // First  Increase Value Then Use
        System.out.println(a);
        
        // For String Value
        char ch = 'a';
        System.out.println(ch++);
        System.out.println(ch);

        char ch1 = 'B';
        System.out.println(ch1);
        System.out.println(--ch1);


    }
}
